	// 필드 이동, 적을 만나면 true 반환
	private boolean move(Field f, int moveX, int moveY) {
		this.moveX = x + moveX;
		this.moveY = y + moveY;
		if (moveY == -1)
			setImage(" Ü ");
		if (moveX == -1)
			setImage(" Ü ");
		if (moveY == 1)
			setImage(" Ü ");
		if (moveX == 1)
			setImage(" Ü ");

		if (this.moveX < f.getW() && this.moveX >= 0 && this.moveY < f.getH() && this.moveY >= 0)

			if (f.checkNullObData(this.moveX, this.moveY, 0)) {
				f.resetOb(this);
				x = this.moveX;
				y = this.moveY;
				f.sortObData(x, y);
				f.sortObData(x - moveX, y - moveY);
			} else if (f.getObData(this.moveX, this.moveY, 0) instanceof Shop) {
				readNum = 0;
				checkShop = true;
			} else if (f.getObData(this.moveX, this.moveY, 0) instanceof Enemi) {
				readNum = 0;
				return true;
			} else if (!f.getObData(this.moveX, this.moveY, 0).collider) {
				f.resetOb(this);
				x = this.moveX;
				y = this.moveY;
				f.sortObData(x, y);
				f.sortObData(x - moveX, y - moveY);
			}
		return false;
	}

