	// 받은 명령어를 하나씩 읽음, 성공적으로 읽으면 true 반환
	public boolean readMoveOrder() {
		if (readNum < sp && readNum < order.length()) {
			read = order.charAt(readNum);
			readNum++;
			return true;
		} else {
			readNum = 0;
			return false;
		}
	}

	// 필드 이동방향 결정
	public boolean runMoveOrder(Field f) {
		switch (read) {
		case 'w':
			return move(f, 0, -1);
		case 'a':
			return move(f, -1, 0);
		case 's':
			return move(f, 0, 1);
		case 'd':
			return move(f, 1, 0);
		default:
			return false;
		}
	}

